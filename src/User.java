public class User {
    private String name;
    private int AccountNo;

    public String getName() {
        return name;
    }
    public void setName(String UserName) {
        name = UserName;
    }

    public int getAccNo() {
        return AccountNo;
    }
    public void setAccNo(int AccNo) {
        AccountNo = AccNo;
    }
}
